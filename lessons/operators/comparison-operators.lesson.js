/* comparison operators */
let goodSwordsmen = 10;
let badMagicians = 8;

let result = goodSwordsmen === badMagicians; // equality

console.log('Are the good guys and bad guys equal in number?', result);

result = goodSwordsmen !== badMagicians; // inequality

console.log('Are the good guys and bad guys different in number?', result);

result = goodSwordsmen > badMagicians; // greater than

console.log('Are there more good guys than bad guys?', result);

result = goodSwordsmen < badMagicians; // less than

console.log('Are there fewer good guys than bad guys?', result);

result = goodSwordsmen >= badMagicians; // greater than or equal to

console.log('Are there at least as many good guys as bad guys?', result);

result = goodSwordsmen <= badMagicians; // less than or equal to

console.log('Are there at least as many bad guys as good guys?', result);