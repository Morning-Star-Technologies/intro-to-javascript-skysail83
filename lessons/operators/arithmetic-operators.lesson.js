/* assignment operator */
let treasureChest = 100; // assignment operator

/* arithmetic operators */
treasureChest = 50 + 50; // addition operator on numbers

console.log('The treasure chest has', treasureChest, 'pieces of gold');

let coinSatchel = 20;

treasureChest = treasureChest + coinSatchel; // addition operator on variables

console.log('The treasure chest now has', treasureChest, 'pieces of gold');

let numberOfThieves = 4;
let thiefCapacity = 25;

treasureChest = treasureChest - (numberOfThieves * thiefCapacity); // subtraction and multiplication operators

console.log('After intense thievery, the treasure chest has', treasureChest, 'pieces of gold left');

let numberOfBeggars = 2;

let beggarsShare = treasureChest / numberOfBeggars; // division operator

console.log('The beggars each receive', beggarsShare, 'pieces of gold');