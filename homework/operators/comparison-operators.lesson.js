let badMagicians = 8;

let result = goodSwordsmen === badMagicians;

console.log('Are the good guys and bad guys equal in number?', result);

result = goodSwordsmen !== badMagicians;

console.log('Are the good guys and bad guys different in number?', result);

result = goodSwordsmen > badMagicians;

console.log('Are there more good guys than bad guys?', result);

result = goodSwordsmen < badMagicians;

console.log('Are there fewer good guys than bad guys?', result);

result = goodSwordsmen >= badMagicians;

console.log('Are there at least as many good guys as bad guys?', result);

result = goodSwordsmen <= badMagicians;

